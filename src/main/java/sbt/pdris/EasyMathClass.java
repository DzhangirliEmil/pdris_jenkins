package sbt.pdris;

public class EasyMathClass {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int multyply(int a, int b) {
        return a*b;
    }

    public static int minus(int a, int b) {
        return a - b;
    }

    public static int square(int a) {
        return a*a;
    }
}
